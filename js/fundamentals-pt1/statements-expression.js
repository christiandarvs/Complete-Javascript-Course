// Expression - that produces a value
console.log(10 + 20);
console.log(2024);
console.log(true || false);

// Statement - bigger piece of code that is executed and doesn't produce a value
const X = 10;
const Y = 20;
if (X > Y) {
  console.log(`${X} > ${Y}`);
} else if (X < Y) {
  console.log(`${X} < ${Y}`);
} else {
  console.log(`${X} == ${Y}`);
}
